// WRITE A PROGRAM TO PRINT YOUR ADRESS 1.USING SUNGLE PRINT 2.USING MULTIPLE PRINTLN
public class practical_2 {
    public static void main(String[] args) {
        // Using single print statement
        System.out.print("My Address: ");
        System.out.print("123 AI Street, ");
        System.out.print("Tech City, ");
        System.out.print("Java Land");

        // Using multiple println statements
        System.out.println("\nMy Address:");
        System.out.println("123 AI Street,");
        System.out.println("Tech City,");
        System.out.println("Java Land");
    }
}
